package com.facility.model;

/**
 * Class represents a room that belongs to a specific building.
 */
public class Room {

    private int id;
    private int name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }
}
